    public static function generateSVG($text, $width = 2, $height = 50) {
        if (empty($text)) return '';
        
        $bars = self::encode($text);
        if (!$bars) return '';
        
        $totalWidth = array_sum($bars) * $width;
        
        $svg = '<svg width="' . $totalWidth . '" height="' . $height . '" xmlns="http://www.w3.org/2000/svg">';
        
        $x = 0;
        $isBar = true;
        
        foreach ($bars as $barWidth) {
            if ($isBar) {
                $svg .= '<rect x="' . $x . '" y="0" width="' . ($barWidth * $width) . '" height="' . $height . '" fill="black"/>';
            }
            $x += $barWidth * $width;
            $isBar = !$isBar;
        }
        
        $svg .= '</svg>';
        return $svg;
    }
    
    private static function encode($text) {
        $bars = [];
        
        // Start pattern
        $bars = array_merge($bars, [2,1,1,4,1,2]);
        
        // Encode each character
        for ($i = 0; $i < strlen($text); $i++) {
            $char = $text[$i];
            if (isset(self::$code128[$char])) {
                $bars = array_merge($bars, self::$code128[$char]);
            }
        }
        
        // Stop pattern
        $bars = array_merge($bars, [2,3,3,1,1,1,2]);
        
        return $bars;
    }
    
    public static function generateBase64($text, $width = 2, $height = 50) {
        $svg = self::generateSVG($text, $width, $height);
        return 'data:image/svg+xml;base64,' . base64_encode($svg);
    }
